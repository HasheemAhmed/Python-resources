# String methods

x = "python is a snake, But it is beautiful."

#capatalize()

print(x.capitalize()) # capatalize the first letter in string

# title()

print(x.title()) # capataliza first letter of all words in a string

# count()

print(x.count("is")) # count specific word in string -- Case Sensitive

#find()

print(x.find("snake")) # return index if find else return -1 - Case Sensitive

#swapcase()

print(x.swapcase()) # convert lower to upper and upper to lower
